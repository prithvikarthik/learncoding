#include <stdio.h>

int main(void)
{
    printf("The size of an int is %ld\n\n", sizeof(int));
    printf("The size of a short is %ld\n\n", sizeof(short));
    printf("The size of a long is %ld\n\n", sizeof(long));
    printf("The size of a float is %ld\n\n", sizeof(float));
    printf("The size of an double is %ld\n\n", sizeof(double));
    printf("The size of an long double is %ld\n\n", sizeof(long double));

    return 0;
}    
